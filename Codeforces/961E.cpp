#define maxn 200005

typedef pair<int, int> pii;

int a[maxn];

int tree[maxn];
void add(int p, int val) {
    for (register int i = p; i < maxn; i += i & -i) tree[i] += val;
    return;
}
int sum(int p) {
    int sum = 0;
    for (register int i = p; i; i -= i & -i) sum += tree[i];
    return sum;
}

set<pii> S;

int main() {
    int n = read<int>();
    for (register int i = 1; i <= n; i++) S.insert((pii){a[i] = read<int>(), i}), add(i, 1);
    long long answer = 0;
    for (register int i = 1; i <= n; i++) {
        while (!S.empty() && S.begin()->first < i) add(S.begin()->second, -1), S.erase(S.begin());
        if (S.find((pii){a[i], i}) != S.end()) S.erase((pii){a[i], i}), add(i, -1);
        answer += sum(min(n, a[i]));
    }
    write(answer), putch('\n');
    return 0;
}