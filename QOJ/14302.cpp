#include <bits/stdc++.h>
using namespace std;

#define endl '\n'

void solve(void) {
    int n, k;
    cin >> n >> k;
    for (int i = 1; i <= k; i++) cout << i << ' ';
    cout << endl;
    return;
}

int main() {
    ios::sync_with_stdio(false), cin.tie(nullptr);

    int _ = 1;
    while (_--) solve();

    return 0;
}
