#define maxn 4000005
#define maxm 20000005

int fa[maxn], siz[maxn], to[maxm], nxt[maxm], head[maxn], ecnt = 0;
bool vis[maxn];

void addEdge(int x, int y) { return to[++ecnt] = y, nxt[ecnt] = head[x], head[x] = ecnt, siz[x]++, void(); }

int cnt1 = 0, cnt2 = 0;
void dfs(int p, int pre = -1) {
    fa[p] = pre, vis[p] = true, cnt1 += (int)siz[p] % 2;
    bool rec = false;
    for (int ei = head[p]; ei; ei = nxt[ei]) {
        int i = to[ei];
        if (!fa[i])
            dfs(i, p);
        else if (vis[i]) {
            if (i == pre && !rec) {
                rec = true;
                continue;
            }
            int tot = (siz[i] >= 3), x = p;
            while (x != i) tot += (siz[x] >= 3), x = fa[x];
            cnt2 += (tot < 2);
        }
    }
    vis[p] = false;
    return;
}

void solve(void) {
    int n = read(), m = read();
    for (int i = 1, x, y; i <= m; i++) x = read(), y = read(), addEdge(x, y), addEdge(y, x);
    dfs(1), write(cnt1 / 2 + cnt2), putch('\n');
    return;
}

